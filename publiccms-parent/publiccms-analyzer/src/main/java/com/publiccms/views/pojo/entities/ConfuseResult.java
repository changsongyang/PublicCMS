package com.publiccms.views.pojo.entities;

public class ConfuseResult {
    private String text;
    private String font;

    /**
     * @return the text
     */
    public String getText() {
        return text;
    }

    /**
     * @param text
     *            the text to set
     */
    public void setText(String text) {
        this.text = text;
    }

    /**
     * @return the font
     */
    public String getFont() {
        return font;
    }

    /**
     * @param font
     *            the font to set
     */
    public void setFont(String font) {
        this.font = font;
    }
}
